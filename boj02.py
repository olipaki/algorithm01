# 알고리즘 분류: 스택(Stack)
# 1. 왜 deque가 아닌 일반 리스트(list)를 스택으로 쓰나요?
#  **스택(Stack)**은 "밑이 막힌 원통형 프링글스 통" 같아요.
# 스택은 한쪽만 써요: 스택은 입구와 출구가 똑같아요. 
# 맨 위에 접시를 쌓고(append), 맨 위에 있는 접시를 다시 빼죠(pop).

# 리스트는 한쪽 끝이 아주 빨라요: 파이썬의 일반 리스트는 맨 뒤에 데이터를 넣거나 빼는 게 
# 빛의 속도만큼 빨라요.
# 결론: 스택은 어차피 맨 뒤(위)만 사용하기 때문에,굳이 양쪽 문이 달린 복잡한 deque를 가져올 필요 없이
# 간편한 일반 리스트를 쓰는 게 국룰(규칙)이 되었답니다!
#####################################################
import sys
# 1. 몇 번 테스트할지 입력받기
T = int(input())

for _ in range(T):
    vps = input() # 괄호 문장을 입력받아요 (예: (()) )
    stack = []
    is_vps = True # 일단은 올바르다고 가정하고 시작!
    
    for char in vps:
        if char == '(':
            stack.append('(') # 여는 괄호는 무조건 쌓기
        else:
            if len(stack) == 0: # 비어있는데 닫는 괄호가 오면
                is_vps = False # 너는 짝꿍이 없구나! 실패!
                break
            else:
                stack.pop() # 짝을 찾았으니 맨 위 접시 빼기
    
    # 문장을 다 읽었는데 접시가 남아있으면? (예: (() )
    if len(stack) > 0:
        is_vps = False
        
    # 결과 발표
    if is_vps:
        print("YES")
    else:
        print("NO")